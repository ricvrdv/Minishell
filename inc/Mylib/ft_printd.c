#include "libft.h"

int	ft_printd(int n)
{
	char	c;
	int		len;

	len = 0;
	if (n == -2147483648)
	{
		return (write(1, "-2147483648", 11));
		len = 11;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		len++;
		n = -n;
	}
	if (n < 10)
	{
		c = n + '0';
		write(1, &c, 1);
		return (len + 1);
	}
	len += ft_printd(n / 10) + 1;
	c = (n % 10) + '0';
	write(1, &c, 1);
	return (len);
}
